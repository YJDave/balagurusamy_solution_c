//Write a program to print the size of various data types in C.

#include<stdio.h>
void main()
{
    printf("Size of Character Data Type : %ld \n",sizeof(char));
    printf("Size of Integer Data Type : %ld \n",sizeof(int));
    printf("Size of Float Data Type : %ld \n",sizeof(float));
    printf("Size of Double Data Type : %ld \n",sizeof(double));
}
